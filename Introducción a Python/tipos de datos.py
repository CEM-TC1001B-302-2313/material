print(type(15)) # int -> integer -> Número entero
print(type(156.363)) # float -> Número real (incluye decimales)
print(type(56j)) # complex -> Número complejo
print(type("Hola mundo")) # str -> string -> texto
print(type(True)) # bool -> booleanos
print(type(False))
print(type([24,22.63,"Hola",True,[1,2]])) # list -> lista
# lista -> Conjunto ordenado y mutable de datos
print(type((13,53,1,True))) # tuple -> tupla
# tupla -> Conjunto ordenado e inmutable de datos
print(type({"hola": [1,2,4], 13: True})) # dict -> diccionario
# diccionario -> Conjunto ordenado, nombrado y mutable de datos
